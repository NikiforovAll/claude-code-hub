// Generates per-app themes.css from themes.json (palette registry ported from
// scratchpad's theme.ts, itself derived from this hub's design tokens).
//
// All sub-apps share the same core variable names, so one palette→var mapping
// serves every app; APP_EXTRAS adds per-app derived vars (e.g. cost's chart
// accent). "ember" is each app's :root default and gets no override block —
// clearing data-color-theme is how you select it.
//
// Usage: node scripts/generate-themes.mjs [app ...]   (default: all apps)

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const themes = JSON.parse(readFileSync(join(root, 'scripts/themes.json'), 'utf8'));

const hexToRgb = (hex) => {
  const n = parseInt(hex.slice(1), 16);
  return `${(n >> 16) & 255}, ${(n >> 8) & 255}, ${n & 255}`;
};

// Core mapping: palette role → shared CSS variable. --accent-glow is mechanical
// (rgba of the accent), mirroring how :root derives it from --accent.
const coreVars = (p, mode) => ({
  '--accent': p.ember,
  '--accent-text': p.emberGlow,
  '--accent-dim': p.emberDim,
  '--accent-glow': `rgba(${hexToRgb(p.ember)}, ${mode === 'dark' ? '0.55' : '0.5'})`,
  '--bg-deep': p.field,
  '--bg-surface': p.surface,
  '--bg-elevated': p.elevated,
  '--bg-hover': p.hover,
  '--border': p.border,
  '--text-primary': p.ink1,
  '--text-secondary': p.ink2,
  '--text-tertiary': p.ink3,
  '--text-muted': p.inkMuted,
});

const APP_EXTRAS = {
  cost: (p) => ({
    '--chart-fill': `rgba(${hexToRgb(p.ember)}, 0.32)`,
    '--chart-1': p.ember,
  }),
  cck: () => ({}),
  memory: () => ({}),
  marketplace: () => ({}),
};

// Selector templates per mode. cck additionally honors OS light preference
// unless dark is forced (matches its prefers-color-scheme block).
const SELECTORS = {
  dark: (id) => `body[data-color-theme="${id}"]:not(.light)`,
  light: (id) => `body.light[data-color-theme="${id}"]`,
};

function vars(app, p, mode) {
  const all = { ...coreVars(p, mode), ...APP_EXTRAS[app](p) };
  return Object.entries(all)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join('\n');
}

function generate(app) {
  let css = `/* GENERATED by scripts/generate-themes.mjs — do not edit by hand.\n   Color themes override only the shared design-token variables; semantic\n   colors (success/warning/error/scope/...) stay constant across themes. */\n`;
  // Picker swatches: every theme (incl. ember) exposes a 3-color preview
  // (surface, accent, ink) as variables; the picker renders them as dots.
  css += '\n/* picker swatches */\n';
  const swatchVars = (p) =>
    `--sw-bg: ${p.surface}; --sw-accent: ${p.ember}; --sw-ink: ${p.ink1}; --sw-border: ${p.border};`;
  for (const t of themes) {
    css += `.theme-swatch-${t.id} { ${swatchVars(t.dark)} }\n`;
    css += `body.light .theme-swatch-${t.id} { ${swatchVars(t.light)} }\n`;
  }

  for (const t of themes) {
    if (t.id === 'ember') continue; // :root default
    css += `\n/* ${t.label} */\n`;
    css += `${SELECTORS.dark(t.id)} {\n${vars(app, t.dark, 'dark')}\n}\n`;
    css += `${SELECTORS.light(t.id)} {\n${vars(app, t.light, 'light')}\n}\n`;
    if (app === 'cck') {
      css += `@media (prefers-color-scheme: light) {\n`;
      css += `body:not(.dark-forced)[data-color-theme="${t.id}"] {\n${vars(app, t.light, 'light')}\n}\n}\n`;
    }
  }
  const out = join(root, app, 'public/themes.css');
  writeFileSync(out, css);
  console.log(`${out}: ${themes.length - 1} themes, ${css.length} bytes`);
}

const apps = process.argv.slice(2).length ? process.argv.slice(2) : Object.keys(APP_EXTRAS);
for (const app of apps) generate(app);
